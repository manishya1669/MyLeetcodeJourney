class Solution{
  public:
    //Function to sort the given linked list using Merge Sort.
    Node* mergeSort(Node* head) {
       Node * curr = head;
       vector<int>v;
       
       while(curr != NULL){
           v.push_back(curr->data);
           curr = curr->next;
       }
       sort(v.begin(),v.end());
       curr = head;
       int i=0;
       while(curr != NULL){
           curr->data = v[i];
           curr= curr->next;
           i++;
       }
       return head;
    }
};


